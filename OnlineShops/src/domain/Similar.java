package domain;

public class Similar {
    private int id;
    private String simi;
    private double score;
    
    public int getId() {
    	return this.id;
    }
    public void setId(int id) {
    	this.id = id;
    }
    
    public String getSimi() {
    	return this.simi;
    }
    public void setSimi(String simi) {
    	this.simi = simi;
    }
    
    public double getScore() {
    	return this.score;
    }
    public void setScore(double score) {
    	this.score = score;
    }
}
